package by.zvezdina.xmltask.exception;

public class FlowerXmlException extends Exception {

    public FlowerXmlException() {
    }

    public FlowerXmlException(String message) {
        super(message);
    }

    public FlowerXmlException(String message, Throwable cause) {
        super(message, cause);
    }

    public FlowerXmlException(Throwable cause) {
        super(cause);
    }
}
